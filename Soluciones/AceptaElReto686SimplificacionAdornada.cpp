bool todas_cifras(int num_1, long long int num_2) {
	int num_marcados = 0;
	vector<bool> marcada(10, false);
	while (num_1 != 0) {
		int cifra = num_1 % 10;
		if (cifra == 0 || marcada[cifra]) return false;
		marcada[cifra] = true;
		num_marcados++;
		num_1 /= 10;
	}
	while (num_2 != 0) {
		int cifra = num_2 % 10;
		if (cifra == 0 || marcada[cifra]) return false;
		marcada[cifra] = true;
		num_marcados++;
		num_2 /= 10;
	}
	return num_marcados == 9;
}


